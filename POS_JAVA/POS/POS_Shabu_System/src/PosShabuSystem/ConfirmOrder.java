    public void setTime() {
            time1.setHorizontalAlignment(SwingConstants.CENTER);
            date1.setHorizontalAlignment(SwingConstants.CENTER);
    
        new Thread(new Runnable() {
            @Override
            public void run() {
                while (true) {
                    try {
                        Thread.sleep(1000);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(ConfirmOrder.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    Date date = new Date();
                    SimpleDateFormat tf = new SimpleDateFormat("h:mm:ss aa", Locale.ENGLISH);
                    SimpleDateFormat df = new SimpleDateFormat("EEEE, dd-MM-yyyy", Locale.ENGLISH);
                    String time = tf.format(date);
                    time1.setText(time.split(" ")[0] + " " + time.split(" ")[1]);
                    date1.setText(df.format(date));
                }
            }
        }).start();
    }




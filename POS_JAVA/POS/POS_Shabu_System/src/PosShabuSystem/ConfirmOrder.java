    private void confirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmActionPerformed
        //convert text to int
        String text = total.getText().toString();
        String totalPriceString = text.replaceAll("[^\\d.]", ""); // extract only digits and decimal point
        double totalPrice = Double.parseDouble(totalPriceString);
        p.settoltal(totalPrice);pay.setVisible(true);
        String orderId = menus.generateOrderId(); 
    
        ///set text
        pay.orderid.setText(orderId);
        pay.vat.setText(String.format("$%s", p.calculatevat()));
        pay.sum.setText(sum.getText());
        pay.sc.setText(String.format("$%s", p.calculatesc()));
        pay.total.setText(String.format("$%.2f", p.calculatetotal()));
        pay.subtotal.setText(String.format("%s", total.getText()));
        pay.qty.setText(qty.getText());
        pay.confirm.setText(String.format("PAY ($%.2f)", p.calculatetotal()));
    
    
    this.setVisible(false);
    }//GEN-LAST:event_confirmActionPerformed

